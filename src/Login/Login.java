    private void Ingresa(String usuario, String pas) {
        String user = "", pass = "";
        try {
            String sql = "SELECT Usuario,Clave FROM UsuarioxRol WHERE Usuario = '" + usuario + "'and Clave = '"+ pas +"'";
            Statement st = cn.createStatement();
            ResultSet rs = st.executeQuery(sql);

            while (rs.next()) {
                user = rs.getString("Usuario"); //Daba error ya que tenia un 20 en lugar del nombre de la columna
                pass = rs.getString("Clave"); //Daba error ya que tenia un 20 en lugar del nombre de la columna
            }

            if (user.equals(usuario) && pass.equals(pas)) {
                this.dispose();
                new Principal().setVisible(true);
            } else {
                this.msj.setVisible(true);
                
                this.msj.setText("¡Usuario/Contraseña incorrectos, intente nuevamente!");
                this.usuario.setText("");
                this.passw.setText("");
                this.usuario.requestFocus();
            }

        } catch (SQLException ex) {
            Logger.getLogger(Login.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

